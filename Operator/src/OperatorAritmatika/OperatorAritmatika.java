package OperatorAritmatika;

public class OperatorAritmatika {
    public static void main(String[] args) {
        System.out.println("operasi Penjumlahan");
        int hasilPenjumlahan = 5 + 1;
        System.out.println("Hasil 5+1 = " + hasilPenjumlahan);
        System.out.println();

        System.out.println("Operasi pengurangan");
        int hasilPengurangan = 5 - 3;
        System.out.println("Hasil 5-3 = " + hasilPengurangan);
        System.out.println();

        System.out.println("Operasi perkalian");
        int hasilPerkalian = 5 * 5;
        System.out.println("Hasil Perkalian 5*5 = "+hasilPerkalian);
        System.out.println();

        System.out.println("Operasi Pembagian");
        int hasilPembagian = 10/2;
        System.out.println("Hasil Pembagian 10/2 = "+hasilPembagian);
        System.out.println();

        System.out.println("Operasi habis bagi");
        int hasilSisa = 8 % 2;
        System.out.println("Hasi; Sisa 8%2 =" + hasilSisa);
        System.out.println();
        int hasilSisa2 = 9 % 2;
        System.out.println("Hasil Sisa 9 % 2 ="+hasilSisa2);
        System.out.println();
    }
}
